//estrutura de dicisão
// switch/case/break/default
let fruta = "banana"
switch (fruta){
    case "laranja":
        console.log("suco de laranja")
    break
    
    case "banana":
        console.log("vitamina de banana")
    break
    
    case "maça":
        console.log("suco de maça")
    break

    default:
        console.log("suco generico")
    break
}